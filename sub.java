public class Subtraction {
    public static void main(String[] args) {
        // Define three variables
        int a = 10;
        int b = 5;
        int c = 2;

        // Perform the subtraction
        int result = a - b - c;

        // Print the result
        System.out.println("The result of " + a + " - " + b + " - " + c +" is: " + result);
    }
}
